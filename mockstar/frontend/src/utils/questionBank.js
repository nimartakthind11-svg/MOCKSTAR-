/**
 * Get a randomized list of questions based on config.
 */
export const getQuestionsForSession = (config) => {
  const { interviewType, difficulty, questionCount } = config;

  let pool = [];

  if (interviewType === "mixed") {
    // For mixed, grab from both technical and behavioral matching the difficulty
    pool = QUESTION_BANK.filter(q => q.difficulty === difficulty);
  } else {
    // Strictly technical or behavioral
    pool = QUESTION_BANK.filter(
      q => q.type === interviewType && q.difficulty === difficulty
    );
  }

  // Shuffle the pool using Fisher-Yates algorithm
  const shuffled = [...pool];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }

  // Return the requested number of questions
  return shuffled.slice(0, questionCount);
};
